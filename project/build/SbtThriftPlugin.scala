import java.io.{File, FileReader, FileWriter}
import java.util.{Date, Properties}
import java.text.SimpleDateFormat
import fm.last.ivy.plugins.svnresolver.SvnResolver
import scala.collection.jcl
import _root_.sbt._

// TODO: somehow link on the real SubversionPublisher in the main source tree
class StandardProjectPlugin(info: ProjectInfo) extends PluginProject(info) with SubversionPublisher {
  override def disableCrossPaths = true

  val env = jcl.Map(System.getenv())

  override def subversionRepository = Some("http://svn.local.twitter.com/maven-public")

  val ivySvn = "ivysvn" % "ivysvn" % "2.1.0" from "http://maven.twttr.com/ivysvn/ivysvn/2.1.0/ivysvn-2.1.0.jar"
  val jruby = "org.jruby" % "jruby-complete" % "1.6.0.RC2"

  override def managedStyle = ManagedStyle.Maven
  def artifactoryRoot = "http://artifactory.local.twitter.com"
  def snapshotDeployRepo = "libs-snapshots-local"
  def releaseDeployRepo = "libs-releases-local"

  override def pomExtra =
    <licenses>
      <license>
        <name>Apache 2</name>
        <url>http://www.apache.org/licenses/LICENSE-2.0.txt</url>
        <distribution>repo</distribution>
      </license>
    </licenses>

  // oh man.
  override def compileAction = super.compileAction dependsOn(makeBanner)

  lazy val makeBanner = task {
    val timestamp = new SimpleDateFormat("yyyy-MM-dd").format(new Date)
    val fileWriter = new FileWriter(new File("src/main/scala/com/twitter/sbt/BuildInfo.scala"))
    fileWriter.write("// AUTOGENERATED BY STANDARD-PROJECT. TURN BACK WHILE YOU STILL CAN!\n")
    fileWriter.write("package com.twitter.sbt\n")
    fileWriter.write("object BuildInfo {\n")
    fileWriter.write("  val version = \"" + version.toString + "\"\n")
    fileWriter.write("  val date = \"" + timestamp + "\"\n")
    fileWriter.write("}\n")
    fileWriter.close()
    None
  }
}
